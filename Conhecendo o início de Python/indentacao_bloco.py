
def sacar(valor):

    saldo = 1000
    
    if saldo >= valor:
        saldo -= valor
        print('OK, tudo certo!')
        print(saldo)
    else:
        saldo < valor
        print ("Ops, algo errado! Não está certo!")

sacar(900)

def depositar(valor):
    saldo = 1000
    saldo += valor
    print(saldo)

depositar(500)






